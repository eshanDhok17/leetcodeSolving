class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        typedef long long ll;
        ll tsum = -1e9, sum = 0;
        for(ll i=0; i<n; i++) {
            sum += arr[i];
            tsum = max(tsum, sum);
            if(sum < 0) sum = 0;
        }
        return tsum;
    }
};

